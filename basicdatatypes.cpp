#include <iostream>
using namespace std;
int main(){
    int a;
    a=12;
    cout<<"size of int"<<sizeof (a)<<endl;
     
    float b;
    b=23;
    cout<<"size of float"<<sizeof (b)<<endl;

    char c;
    c=13;
    cout<<"size of char"<<sizeof (c)<<endl;

    bool d;
    d=43;
    cout<<"size of bool"<<sizeof (d)<<endl;

    short int si;
    long int li;
    cout<<"size of shortint "<<sizeof (si)<<endl;
    cout<<"size of longint"<<sizeof (li)<<endl;
return 0;

}